package sda.calculator;

public class Calculator {
    public int sum(int a, int b) {
        return a + b;
    }
    public int divide1(int a, int b) {
        return a * b;
    }
    public int substract(int a, int b) {
        return a - b;
    }
    public int multiply(int a, int b) {
        return a / b;
    }
    }

